package fi.konstal.example.game1.util;

/**
 * A state indicating the current direction faced.
 *
 *
 * @author Konsta Lehtinen
 * @version 2017-12-20
 */
public enum DirectionState {

    /**
     * Right direction state.
     */
    RIGHT,

    /**
     * Left direction state.
     */
    LEFT,

    /**
     * Up direction state.
     */
    UP,

    /**
     * Down direction state.
     */
    DOWN;
}
